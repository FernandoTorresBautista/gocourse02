package main

import "fmt"

func main() {
	a := 2
	// a = a + 2
	fmt.Println(a)

	// operadores de asignacion

	// suma en asignacion
	a += 2
	fmt.Println(a)

	// resta en asignacion
	a -= 2
	fmt.Println(a)

	// multiplicacion en asignacion
	a *= 2
	fmt.Println(a)

	// division en asignacion
	a /= 2
	fmt.Println(a)

	// modulo en asignacion
	a %= 2
	fmt.Println(a)

}
