package main

import "fmt"

func main() {
	// comparar valores
	a := 4
	b := 5

	fmt.Println(a == b) // iguales
	fmt.Println(a != b) // distintos
	fmt.Println(a > b)  // mayor que
	fmt.Println(a < b)  // menor que
	fmt.Println(a >= b) // mayor o igual que
	fmt.Println(a <= b) // menor  o igual que
}
